# --------------------------- DESAFIO ----------------------------

# ( , ) A vírgula é usada principalmente para separar itens.
print("Python", "é", "demais")


# ( + ) O sinal de mais tem como função juntar os textos 
print("Python" + "é" + "demais")


# ( end ) É um parâmetro usado dentro do print que serve para determinar como a frase termina
# ( ; ) Serve para colocar mais de um comando na mesma linha.
print("Linha 1", end=" --- "); print("Linha 2")


# ( sup ) É um parâmetro usado dentro do print que serve para escolher o que vai separar os valores 
print("Item A", "Item B", "Item C", sep=" | ")


# ( \n ) É um caractere especial que serve para quebrar linha
print("Menu \n 1. Iniciar \n 2. Sair ")

# ( r ) Serve para dizer ao Python que a string é “crua”, ou seja, os caracteres especiais não serão interpretados.
print(r"C:\usuarios\nome\documentos")


